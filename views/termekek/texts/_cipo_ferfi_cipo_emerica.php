Az ízig vérig skate márkákat teremtő Sole Technology legropogósabb brandje 1996-ban látta meg a napvilágot, mint a gördeszkás footwear új generációja. 
Az Emerica cipők deszkázásra és utcai viseletre is első osztályú. Harmonizáló földszíneket, letisztult vonalakat és sportos összhatást kapunk az Emericától. Válaszd ki a kedvenced széles kínálatunkból, rendeld meg, holnap pedig már taposhatod is a mókuskereket egy nem hétköznapi cipőben.
