Etnies a te cipőd, ha a gördeszkás nagyhatalomra vágysz.
<br />
Az Etnies cég talán egyedülállóként mondhatja el magáról, hogy tulajdonosai a mai napig aktív deszkások. Gördeszkázásra tervezett cipőik a környezetre is jótékony hatással 
vannak, ugyanis a „Buy a shoe, plant a tree” mozgalmuk keretében minden eladott cipő után elültetnek egy fát az Amazonas őserdőben. Így nemcsak állati menő lépőd lesz, de a természettel is jót tettél. Az Etnies cipőkért görgess lejjebb!
</p>