<p>

1966. március 16-án néhány lelkes fiatal cipőboltot nyitott a kaliforniai Anaheim-ben. Azon a reggelen 12 rendelést kaptak és délutánra már le is gyártotta cipőjüket a Van Doren Rubber Company. Így ők 12-en lettek a legelső boldog Vans cipő tulajdonosok. 
<a href="#" data-toggle="collapse" data-target="#viewdetails" style="color:#2A87E4;">Tovább...</a>

<br />

<span class="collapse" id="viewdetails">
A kezdeti cipők után, a kora '70-es években jelent meg #44 névre keresztelt modellük, melyet ma az ikonikus <a href="/cipo/vans?q=Authentic">Vans Authentic</a> cipőként ismerünk. 

A kaliforniai gördeszkások körében hamar népszerű lett és az 1976-ban megszületett <a href="/cipo/vans?q=Era">Vans Era</a> tervezésében már a kor népszerű gördeszkásai, Tony Alva és Stacy Peralta is segítettek.
<br />			
Az Era-t egy évvel később, 1977-ben a követte a <a href="/cipo/vans?q=Old Skool">Vans Old Skool</a>, melyen először jelent meg a védjeggyé vált oldalcsík (Sidestripe). Az Old Skool vitathatatlanul a Vans legsikeresebb cipőjévé vált. A szintén ikonikus modellek sorát bővítette a  <a href="/cipo/vans?q=Slip-On">Vans Slip-On</a>, majd a '78-ban, akkor még "Style 38" néven megszületett magasszárú <a href="/cipo/vans?q=Sk8-Hi">Vans Sk8-Hi</a>.
<br />	
1988-ban kiadásra került az első vízhatlan anyagból készült modell <a href="/cipo/vans?q=MTE">Mountain Edition (MTE)</a> néven. Szintén ebben az évben egy újabb ikon is született: Half Cab, Steve Caballero signature modellje, mely az első pro gördeszkás cipő volt a piacon.
<br />			
<br />
A Vans hatása nem csak a gördeszkázásra, hanem a zenei életre is megkérdőjelezhetetlen. A '90-es években fő támogatóként szállt be a Warped Tour-ba, ezzel számtalan pop, rock, punk zenekart támogatva és erősítve cipőikkel kifejezett kaliforniai életérzést. 1998-ban hazánkat is érintette a turné, ahol többek közt Steve Caballero is népszerüsítette a márkát.
<br />
<br />
</span>
			
